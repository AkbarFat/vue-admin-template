/**
 * @param {string} path
 * @returns {Boolean}
 */
export function isExternal(path) {
  return /^(https?:|mailto:|tel:)/.test(path)
}

/**
 * @param {string} str
 * @returns {Boolean}
 */
export function validPhoneNumber(str) {
  const phoneRegex = /^[0-9]{9}$/
  return phoneRegex.test(str.trim())
}
